import numpy as np

x = 1.0
y = 2.0

#trigonometry
print(np.sin(x), " sin")
print(np.cos(x), " cos")
print(np.tan(x), " tan")
print(np.arcsin(x), " arcsin")
print(np.arccos(x), " arccos")
print(np.arctan(x), " arctan")
print(np.arctan2(x,y), " arctan2")
print(np.rad2deg(x), " radians to degrees")

#hyperbolic functions
print(np.sinh(x), " sinh")
print(np.cosh(x), " cosh")
print(np.tanh(x), " tanh")
print(np.arcsinh(x), " arcsinh")
print(np.arccosh(x), " arccosh")
print(np.arctanh(x), " arctanh")